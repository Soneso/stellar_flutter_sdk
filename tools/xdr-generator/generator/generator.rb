  def render_array_typedef_txrep_methods(out, class_name, element_type, is_fixed, size)
    field_name = underscore_field(class_name)
    out.puts ""
    out.puts "  void toTxRep(String prefix, List<String> lines) {"
    unless is_fixed
      out.puts "    lines.add('\$prefix.len: \${_#{field_name}.length}');"
    end
    out.puts "    for (int i = 0; i < _#{field_name}.length; i++) {"
    txrep_to_line(out, "_#{field_name}[i]", element_type, "$prefix[$i]", "      ")
    out.puts "    }"
    out.puts "  }"
    out.puts ""
    out.puts "  static #{class_name} fromTxRep(Map<String, String> map, String prefix) {"
    if is_fixed
      out.puts "    List<#{element_type}> items = [];"
      out.puts "    for (int i = 0; i < #{size}; i++) {"
      out.puts "      items.add(#{txrep_from_expr(element_type, "map", "$prefix[$i]")});"
      out.puts "    }"
    else
      out.puts "    int len = TxRepHelper.parseInt(TxRepHelper.getValue(map, '\$prefix.len') ?? '0');"
      out.puts "    List<#{element_type}> items = [];"
      out.puts "    for (int i = 0; i < len; i++) {"
      out.puts "      items.add(#{txrep_from_expr(element_type, "map", "$prefix[$i]")});"
      out.puts "    }"
    end
    out.puts "    return #{class_name}(items);"
    out.puts "  }"
  end

  # ---------------------------------------------------------------------------
  # TxRep primitive helpers
  # ---------------------------------------------------------------------------

  # Emit a single lines.add() call for toTxRep.
  # out: output stream, accessor: Dart expression, dart_type: resolved type string,
  # prefix_expr: the TxRep key (may contain Dart interpolation like $prefix.field),
  # indent: leading whitespace string.
  def txrep_to_line(out, accessor, dart_type, prefix_expr, indent)
    # Strip nullable
    base_type = dart_type.sub(/\?\z/, '')

    if TXREP_COMPACT_TYPES.key?(base_type)
      fmt = TXREP_COMPACT_TYPES[base_type][:format]
      out.puts "#{indent}lines.add('#{prefix_expr}: \${#{fmt}(#{accessor})}');"
      return
    end

    case base_type
    when "int"
      out.puts "#{indent}lines.add('#{prefix_expr}: \${#{accessor}}');"
    when "BigInt"
      out.puts "#{indent}lines.add('#{prefix_expr}: \${#{accessor}}');"
    when "bool"
      out.puts "#{indent}lines.add('#{prefix_expr}: \${#{accessor}}');"
    when "double"
      out.puts "#{indent}lines.add('#{prefix_expr}: \${#{accessor}}');"
    when "String"
      out.puts "#{indent}lines.add('#{prefix_expr}: \${TxRepHelper.escapeString(#{accessor})}');"
    when "Uint8List"
      out.puts "#{indent}lines.add('#{prefix_expr}: \${TxRepHelper.bytesToHex(#{accessor})}');"
    else
      if base_type =~ /\AList<(.+)>\z/
        elem = $1
        out.puts "#{indent}lines.add('#{prefix_expr}.len: \${#{accessor}.length}');"
        out.puts "#{indent}for (int i = 0; i < #{accessor}.length; i++) {"
        txrep_to_line(out, "#{accessor}[i]", elem, "#{prefix_expr}[$i]", "#{indent}  ")
        out.puts "#{indent}}"
      else
        # Named XDR type — delegate to its toTxRep method
        out.puts "#{indent}#{accessor}.toTxRep('#{prefix_expr}', lines);"
      end
    end
  end

  # Return a Dart expression for fromTxRep of the given type.
  def txrep_from_expr(dart_type, map_var, prefix_expr)
    base_type = dart_type.sub(/\?\z/, '')

    if TXREP_COMPACT_TYPES.key?(base_type)
      parse = TXREP_COMPACT_TYPES[base_type][:parse]
      return "#{parse}(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? '')"
    end

    case base_type
    when "int"
      "TxRepHelper.parseInt(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? '0')"
    when "BigInt"
      "TxRepHelper.parseBigInt(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? '0')"
    when "bool"
      "(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? 'false') == 'true'"
    when "double"
      "double.parse(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? '0')"
    when "String"
      "TxRepHelper.unescapeString(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? '')"
    when "Uint8List"
      "TxRepHelper.hexToBytes(TxRepHelper.getValue(#{map_var}, '#{prefix_expr}') ?? '')"
    else
      "#{base_type}.fromTxRep(#{map_var}, '#{prefix_expr}')"
    end
  end

  # Generate a parameter name for encode methods
  # XdrPrice -> encodedPrice, XdrLedgerHeader -> encodedLedgerHeader
  def encode_param_name(class_name)
    short = class_name.sub(/\AXdr/, '').sub(/Base\z/, '')
    "encoded#{short}"
  end

  def decode_var_name(class_name)
    short = class_name.sub(/\AXdr/, '').sub(/Base\z/, '')
    "decoded#{short}"
  end

  # Generate field name from class name for typedefs
  # XdrUint32 -> uint32, XdrHash -> hash, XdrString32 -> string32
  def underscore_field(class_name)
    short = class_name.sub(/\AXdr/, '')
    short[0].downcase + short[1..]
  end

  def resolve_size(decl)
    case decl
    when AST::Declarations::Opaque
      decl.size
    when AST::Declarations::Array
      _, size = decl.type.array_size
      size
    else
      decl.size
    end
  end

  def typedef_is_optional?(type)
    return false unless type.is_a?(AST::Typespecs::Simple)
    resolved = type.resolved_type
    return false unless resolved.is_a?(AST::Definitions::Typedef)
    resolved_name = name(resolved)
    return false if TYPE_OVERRIDES.key?(resolved_name)
    resolved.declaration.type.sub_type == :optional
  rescue
    false
  end

  # Resolve typedef to Dart type info for simple typedefs
  def resolve_typedef_type(underlying)
    case underlying
    when AST::Typespecs::Int
      {
        dart_type: "int",
        imports: ["xdr_data_io.dart"],
        encode: ->(param, field) { "stream.writeInt(#{param}.#{field})" },
        decode: -> { "stream.readInt()" },
      }
    when AST::Typespecs::UnsignedInt
      {
        dart_type: "int",
        imports: ["xdr_data_io.dart"],
        encode: ->(param, field) { "stream.writeInt(#{param}.#{field})" },
        decode: -> { "stream.readInt()" },
      }
    when AST::Typespecs::Hyper
      {
        dart_type: "BigInt",
        imports: ["xdr_data_io.dart"],
        encode: ->(param, field) { "stream.writeBigInt64(#{param}.#{field})" },
        decode: -> { "stream.readBigInt64Signed()" },
      }
    when AST::Typespecs::UnsignedHyper
      {
        dart_type: "BigInt",
        imports: ["xdr_data_io.dart"],
        encode: ->(param, field) { "stream.writeBigInt64(#{param}.#{field})" },
        decode: -> { "stream.readBigInt64()" },
      }
    when AST::Typespecs::Bool
      {
        dart_type: "bool",
        imports: ["xdr_data_io.dart"],
        encode: ->(param, field) { "stream.writeBoolean(#{param}.#{field})" },
        decode: -> { "stream.readBoolean()" },
      }
    when AST::Typespecs::Simple
      resolved = underlying.resolved_type
      if resolved.is_a?(AST::Definitions::Typedef)
        inner_decl = resolved.declaration
        # Only resolve through if inner typedef is a primitive-style type
        # (has a .type we can recurse on). For leaf typedefs (opaque, string,
        # array), use the named wrapper class instead.
        if inner_decl.respond_to?(:type) && !inner_decl.is_a?(AST::Declarations::Opaque) &&
           !inner_decl.is_a?(AST::Declarations::String) && !inner_decl.is_a?(AST::Declarations::Array)
          return resolve_typedef_type(inner_decl.type)
        end
        # Fall through to use the resolved typedef as a named type
      end
      # Named type reference
      dart_name = name(resolved)
      if TYPE_OVERRIDES.key?(dart_name)
        dart_name = TYPE_OVERRIDES[dart_name]
      end
      {
        dart_type: dart_name,
        imports: ["xdr_data_io.dart"] + collect_type_imports(dart_name),
        encode: ->(param, field) { "#{dart_name}.encode(stream, #{param}.#{field})" },
        decode: -> { "#{dart_name}.decode(stream)" },
      }
    else
      nil
    end
  end

